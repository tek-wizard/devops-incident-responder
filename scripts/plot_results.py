from __future__ import annotations

import argparse
import json
from pathlib import Path


def load_payload(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def main() -> None:
    parser = argparse.ArgumentParser(description="Plot baseline / training evaluation summaries.")
    parser.add_argument("--input", type=Path, required=True, help="Path to eval JSON generated by scripts/eval.py")
    parser.add_argument("--output-dir", type=Path, default=Path("artifacts/plots"))
    args = parser.parse_args()

    try:
        import matplotlib.pyplot as plt
    except Exception as exc:  # pragma: no cover - depends on runtime image
        raise RuntimeError("matplotlib is required to render plots. Install it in the hackathon environment.") from exc

    payload = load_payload(args.input)
    by_task = payload["summary"]["by_task"]
    tasks = list(by_task.keys())
    solve_rates = [by_task[task]["solve_rate"] for task in tasks]
    avg_rewards = [by_task[task]["avg_reward"] for task in tasks]

    args.output_dir.mkdir(parents=True, exist_ok=True)

    plt.figure(figsize=(10, 4))
    plt.bar(tasks, solve_rates, color="#2b6cb0")
    plt.xticks(rotation=30, ha="right")
    plt.ylim(0, 1.05)
    plt.ylabel("Solve Rate")
    plt.title(f"Solve Rate by Task ({payload['policy']} / {payload['seed_set']})")
    plt.tight_layout()
    plt.savefig(args.output_dir / "solve_rate.png")
    plt.close()

    plt.figure(figsize=(10, 4))
    plt.bar(tasks, avg_rewards, color="#2f855a")
    plt.xticks(rotation=30, ha="right")
    plt.ylabel("Average Reward")
    plt.title(f"Average Reward by Task ({payload['policy']} / {payload['seed_set']})")
    plt.tight_layout()
    plt.savefig(args.output_dir / "avg_reward.png")
    plt.close()

    print(f"Saved plots to {args.output_dir}")


if __name__ == "__main__":
    main()
